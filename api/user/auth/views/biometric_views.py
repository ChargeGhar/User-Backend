@biometric_router.register(r"auth/biometric/status", name="biometric-status")
@extend_schema(
    tags=["Authentication - Biometric"],
    summary="Check Biometric Status",
    description="Check if biometric is enabled for a specific device",
    parameters=[
        OpenApiParameter(
            name='device_id',
            type=str,
            location=OpenApiParameter.QUERY,
            required=True,
            description='Device ID to check biometric status for'
        )
    ],
    responses={
        200: BaseResponseSerializer,
        400: BaseResponseSerializer
    }
)
class BiometricStatusView(GenericAPIView, BaseAPIView):
    permission_classes = [IsAuthenticated]
    
    @log_api_call()
    def get(self, request: Request) -> Response:
        """Get biometric status for device"""
        device_id = request.query_params.get('device_id')
        if not device_id:
            return Response(
                {
                    'success': False,
                    'error': {
                        'code': 'validation_error',
                        'message': 'device_id query parameter is required'
                    }
                },
                status=status.HTTP_400_BAD_REQUEST
            )
        
        return self.handle_service_operation(
            lambda: BiometricAuthService().get_biometric_status(
                user=request.user,
                device_id=device_id
            ),
            success_message="Biometric status retrieved"
        )
